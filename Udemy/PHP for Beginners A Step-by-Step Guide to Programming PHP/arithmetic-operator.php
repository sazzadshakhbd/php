<?php 
  $x = 10;
  $y = 5;

  // Addition 
  echo $x + $y;
  echo "<br>";

  // Substraction 
  echo $x - $y;
  echo "<br>";

  // Multiplication 
  echo $x * $y;
  echo "<br>";

  // Division 
  echo $x / $y;
  echo "<br>";

  // Modulas 
  echo $x % $y;
  echo "<br>";

  // pre increment
  echo ++$x;
  echo "<br>";

  // pre decrement 
  echo --$y;
  echo "<br>";

?>